function generateNumberRange(start, end, step = 1, appendPercentage = false) {
    let range = [];
    for (let i = start; i <= end; i += step) {
        let value = i.toFixed(step < 1 ? 1 : 0);
        if (appendPercentage) {
            value += '%';
        }
        range.push(value);
    }
    return range;
}
